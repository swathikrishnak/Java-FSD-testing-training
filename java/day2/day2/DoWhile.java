package day2;



public class DoWhile {

	public static void main(String[] args) {
		//PROGRAMME TO PRINT 1 TO 10
		int i=1;
		do {
			System.out.println(i);
			i++;
		}while(i<=10);
		
		//PROGRAMME TO PRINT FROM 10 TO 1
		int j=10;
		do {
			System.out.println(j);
			j--;
			
		}while(j>=1);
		
		
		}

}
